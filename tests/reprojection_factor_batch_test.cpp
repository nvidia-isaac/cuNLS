/**
 * @file reprojection_factor_batch_test.cpp
 * @brief Unit tests for ReprojectionFactorBatch.
 *
 * Tests the reprojection factor by creating synthetic 3D points and
 * camera poses, projecting points to create observations in normalized
 * coordinates, then verifying that optimization can recover disturbed
 * states (either points or poses).
 */

#include "cunls/factor/reprojection_factor_batch.h"

#include <gtest/gtest.h>

#include <cmath>
#include <random>
#include <vector>

#include "cunls/common/cublas_helper.h"
#include "cunls/common/cuda_stream.h"
#include "cunls/minimizer/gauss_newton_minimizer.h"
#include "cunls/common/helper.h"
