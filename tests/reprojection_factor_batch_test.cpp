/**
 * @brief Tests that the factor correctly handles points behind the
 * camera.
 *
 * Verifies that points with depth below z_threshold produce zero residuals
 * and zero Jacobians to avoid numerical instability.
 */
TEST_F(ReprojectionFactorBatchTest, ZThresholdHandling) {
  auto test_range = profiler_domain_.CreateDomainRange("ZThresholdHandling");

  // Create a simple test case with one pose and one point
  // Position the point behind the camera (negative z in camera frame)
  SE3Transform identity_pose;
  identity_pose.fill(0.0f);
  identity_pose[0] = 1.0f;   // R(0,0)
  identity_pose[5] = 1.0f;   // R(1,1)
  identity_pose[10] = 1.0f;  // R(2,2)
  identity_pose[15] = 1.0f;  // homogeneous

  Point3D point_behind;
  point_behind[0] = 1.0f;
  point_behind[1] = 1.0f;
  point_behind[2] = -1.0f;  // Behind camera (negative z)

  Observation2D dummy_obs;
  dummy_obs[0] = 0.0f;
  dummy_obs[1] = 0.0f;

  // Copy to device
  dvector<SE3Transform> pose_device({identity_pose});
  dvector<Point3D> point_device({point_behind});
  dvector<Observation2D> obs_device({dummy_obs});

  // Create factor batch with explicit positive z_threshold to test the check
  constexpr float z_threshold_for_test = 1e-3f;
  ReprojectionFactorBatch factor_batch(cublas_handle_, obs_device.data(), 1,
                                              z_threshold_for_test);

  // Set up state pointers
  std::vector<const float*> param_ptrs = {
      reinterpret_cast<const float*>(pose_device.data()),
      reinterpret_cast<const float*>(point_device.data())};
  dvector<const float*> param_ptrs_device(param_ptrs);

  // Allocate outputs
  dvector<float> residuals(2);
  dvector<float> jacobians(2 * 9);

  CudaStream stream;
  factor_batch.Evaluate(residuals.data(), jacobians.data(),
                         param_ptrs_device.data(), stream.GetStream());
  THROW_ON_CUDA_ERROR(cudaStreamSynchronize(stream.GetStream()));

  // Verify residuals are zero (point behind camera)
  std::vector<float> host_residuals(2);
  residuals.CopyToHost(host_residuals.data(), 2);

  EXPECT_FLOAT_EQ(host_residuals[0], 0.0f) << "Residual x should be zero";
  EXPECT_FLOAT_EQ(host_residuals[1], 0.0f) << "Residual y should be zero";

  // Verify Jacobians are zero
  std::vector<float> host_jacobians(2 * 9);
  jacobians.CopyToHost(host_jacobians.data(), 2 * 9);

  for (int i = 0; i < 2 * 9; i++) {
    EXPECT_FLOAT_EQ(host_jacobians[i], 0.0f)
        << "Jacobian element " << i << " should be zero";
  }
}

